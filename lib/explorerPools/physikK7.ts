type LangEntry = { en: string; de: string; hu: string; ro: string };

const lang = (en: string, de: string, hu: string, ro: string): LangEntry => ({ en, de, hu, ro });

const makeLabels = (entries: Record<string, LangEntry>): Record<string, Record<string, string>> => {
  const out: Record<string, Record<string, string>> = { en: {}, de: {}, hu: {}, ro: {} };
  for (const [key, value] of Object.entries(entries)) {
    out.en[key] = value.en;
    out.de[key] = value.de;
    out.hu[key] = value.hu;
    out.ro[key] = value.ro;
  }
  return out;
};

const icon = (icon: string, title: string, bg: string, color: string) => ({ type: "simple-icon", icon, title, bg, color } as const);

